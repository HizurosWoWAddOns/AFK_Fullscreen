	local all = C_CVar.GetCVar(sound_channel_all)
	local status = all and "" or "(Currently all muted!)";
	local l = {};
	for i, v in ipairs(sound_channels) do
		if _G[v[1]] then
			if status=="" and v[2]~=true and C_CVar.GetCVar(v[2])=="0" then
				status = "(Currently muted!)";
