local function exclude(str)
	for _,pat in ipairs(sk_exclude)do
		if type(pat)=="table" then
			local firstMatch = false;
			for i=1, #pat do
				if str:match(pat[i]) then
					if i==1 then
						firstMatch=true;
					elseif firstMatch==true then
						return true; -- only second match return true; lua regexp has no multible match syntax like "^CONTENT_TRACKING_.*_(END|START|STOP_TRACKING)$"
					end
				end
			end
		elseif str:match(pat) then
			return true;
		end
	end

	--[[
	for k,v in pairs(sk_exclude) do
		if str:match(k) then
			if v==1 then
				return true;
			else
				for K,V in pairs(v) do
					if str:match(K) and V==1 then
						return true;
					end
				end
			end
		end
	end
	--]]
	return false;
end

